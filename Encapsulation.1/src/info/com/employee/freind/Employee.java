package info.com.employee.freind;

public class Employee {
	public int id;
	public String name;
	private int salary;
	
	public int getId() {
		return id;
	
	}
	
	public String getName() {
		return name;
	
	}
	
	public int getSalary() {
		return salary;
	
	}
	
	public void setId(int id) {
		this.id=id;
	}
	
	public void setName(String name) {
		this.name=name;
	}
	
	public void setSalary(int salary) {
		this.salary=salary;
	}
	
	

}
